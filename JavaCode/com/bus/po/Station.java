package com.bus.po;

public class Station {

	private String station_name;
	private String baidu_lng;
	private String baidu_lat;
	private String sjcc;
	private String route_count;
	public String getStation_name() {
		return station_name;
	}
	public void setStation_name(String station_name) {
		this.station_name = station_name;
	}
	public String getBaidu_lng() {
		return baidu_lng;
	}
	public void setBaidu_lng(String baidu_lng) {
		this.baidu_lng = baidu_lng;
	}
	public String getBaidu_lat() {
		return baidu_lat;
	}
	public void setBaidu_lat(String baidu_lat) {
		this.baidu_lat = baidu_lat;
	}
	public String getSjcc() {
		return sjcc;
	}
	public void setSjcc(String sjcc) {
		this.sjcc = sjcc;
	}
	public String getRoute_count() {
		return route_count;
	}
	public void setRoute_count(String route_count) {
		this.route_count = route_count;
	} 
	
	
}
